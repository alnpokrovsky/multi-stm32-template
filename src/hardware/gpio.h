#ifndef __HARDWARE_GPIO_H__
#define __HARDWARE_GPIO_H__

#include <stdint.h>

typedef enum {
    GPIO_INPUT,
    GPIO_OUTPUT,
} PIN_MODES;

typedef enum {
    PA_0  = 0x00,
    PA_1  = 0x01,
    PA_2  = 0x02,
    PA_3  = 0x03,
    PA_4  = 0x04,
    PA_5  = 0x05,
    PA_6  = 0x06,
    PA_7  = 0x07,
    PA_8  = 0x08,
    PA_9  = 0x09,
    PA_10 = 0x0A,
    PA_11 = 0x0B,
    PA_12 = 0x0C,
    PA_13 = 0x0D,
    PA_14 = 0x0E,
    PA_15 = 0x0F,

    PB_0  = 0x10,
    PB_1  = 0x11,
    PB_2  = 0x12,
    PB_3  = 0x13,
    PB_4  = 0x14,
    PB_5  = 0x15,
    PB_6  = 0x16,
    PB_7  = 0x17,
    PB_8  = 0x18,
    PB_9  = 0x19,
    PB_10 = 0x1A,
    PB_11 = 0x1B,
    PB_12 = 0x1C,
    PB_13 = 0x1D,
    PB_14 = 0x1E,
    PB_15 = 0x1F,

    PC_0  = 0x20,
    PC_1  = 0x21,
    PC_2  = 0x22,
    PC_3  = 0x23,
    PC_4  = 0x24,
    PC_5  = 0x25,
    PC_6  = 0x26,
    PC_7  = 0x27,
    PC_8  = 0x28,
    PC_9  = 0x29,
    PC_10 = 0x2A,
    PC_11 = 0x2B,
    PC_12 = 0x2C,
    PC_13 = 0x2D,
    PC_14 = 0x2E,
    PC_15 = 0x2F,
} PIN_NAMES;

/**
 * set input(pull down) or output(pushpull) mode for pin
 * and activate its periph rcc
 */
void gpio_pin_mode(PIN_NAMES pn, PIN_MODES pmode);

/**
 * set pin state (bool)
 */
void gpio_pin_set(PIN_NAMES pn, uint16_t state);

/**
 * toggle pin state
 */
void gpio_pin_toggle(PIN_NAMES pn);

/**
 * get pin state (bool)
 */
uint16_t gpio_pin_get(PIN_NAMES pn);

#endif
